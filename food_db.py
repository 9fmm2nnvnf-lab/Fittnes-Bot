import re

def parse_food_query(text: str):
    """
    Парсит запрос вида 'гречка 200г' или 'куриная грудка 150 г'.
    Возвращает (product_name, grams) или None.
    """
    text = text.lower().strip()

    # Ищем число с 'г' или 'гр' или 'грамм'
    match = re.search(r'(\d+\.?\d*)\s*(?:г|гр|грамм|g)', text)
    grams = float(match.group(1)) if match else 100.0

    # Убираем число и единицы из текста — остаётся название
    name = re.sub(r'\d+\.?\d*\s*(?:г|гр|грамм|g)', '', text).strip()

    return name, grams


def find_product(query: str):
    """
    Ищет продукт в базе. Возвращает (key, (kcal,p,f,c)) или None.
    Сначала точное совпадение, потом частичное.
    """
    q = query.lower().strip()

    # Точное совпадение
    if q in FOOD_DB:
        return q, FOOD_DB[q]

    # Частичное совпадение — ищем по подстроке
    matches = [(k, v) for k, v in FOOD_DB.items() if q in k or k in q]
    if matches:
        # Берём самое короткое совпадение (наиболее точное)
        return min(matches, key=lambda x: len(x[0]))

    # Поиск по первому слову
    first_word = q.split()[0] if q.split() else q
    matches = [(k, v) for k, v in FOOD_DB.items() if first_word in k]
    if matches:
        return min(matches, key=lambda x: len(x[0]))

    return None


def calculate_portion(kcal_per100, p_per100, f_per100, c_per100, grams):
    """Считает КБЖУ для заданной порции."""
    factor = grams / 100
    return {
        "kcal":    round(kcal_per100 * factor),
        "protein": round(p_per100    * factor, 1),
        "fat":     round(f_per100    * factor, 1),
        "carbs":   round(c_per100    * factor, 1),
    }


def analyze_food_text(text: str):
    """
    Главная функция — принимает текст, возвращает dict с КБЖУ.
    Совместима с интерфейсом ai_helper.
    """
    name, grams = parse_food_query(text)
    result = find_product(name)

    if not result:
        return None

    product_key, (kcal100, p100, f100, c100) = result
    portion = calculate_portion(kcal100, p100, f100, c100, grams)

    comments = {
        "healthy": "Отличный выбор — герой в восторге! 🏆",
        "junk":    "Вкусно, но изредка 😅",
        "neutral": "Нормально, герой поел.",
    }

    # Определяем категорию по ккал/белкам
    if p100 >= 15 and kcal100 < 300:
        category = "healthy"
    elif kcal100 > 400:
        category = "junk"
    else:
        category = "neutral"

    return {
        "name":    f"{product_key.capitalize()} {int(grams)}г",
        "kcal":    portion["kcal"],
        "protein": portion["protein"],
        "fat":     portion["fat"],
        "carbs":   portion["carbs"],
        "comment": comments[category],
    }

